package org.example;

public class Length {
    public static void main(String[] args) {

        String printText = "რეგრესიული ტესტირება არის ჩასატარებელი";

        if (printText.length() % 2 == 0){
            System.out.println("ლუწი");
        }
        else {
            System.out.println("კენტი");
        }
    }
}




